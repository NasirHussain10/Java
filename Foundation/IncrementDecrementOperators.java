public class IncrementDecrementOperators {
    public static void main(String[] args) {
        int d = 5;
        
        d++;
        System.out.println(d);
        
        d--;
        System.out.println(d);

        ++d;
        System.out.println(d);
        
        --d;
        System.out.println(d);
    }
}
